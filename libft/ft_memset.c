#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned long	i;
	char			*copy;

	i = 0;
	copy = b;
	while (i < len)
	{
		copy[i] = (char)c;
		i++;
	}
	return (copy);
}
